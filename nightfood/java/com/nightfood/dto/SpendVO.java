package com.nightfood.dto;

public class SpendVO {
	private String out_money;
	private String create_date;
	private String idx;
	private String money_idx;
	public String getOut_money() {
		return out_money;
	}
	public void setOut_money(String out_money) {
		this.out_money = out_money;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getMoney_idx() {
		return money_idx;
	}
	public void setMoney_idx(String money_idx) {
		this.money_idx = money_idx;
	}
	
}
